long long pow(long long n, long long m, long long mod = 0){
	if(m < 0) return 0;
	long long ans = 1;
	long long k = n;
	while(m){
		if(m & 1) {
			ans *= k;
			if(mod) ans %= mod;
		}
		k *= k;
		if(mod) k %= mod;
		m >>= 1;
	}
	return ans;
}

struct BearPlays{
	int pileSize(int a, int b, int K){
		int n = a + b;
		a = a * pow(2, K, b+ a) % (b + a);

		return min(a, n - a);
	}
	


};




